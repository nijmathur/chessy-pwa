/**
 * Convert algebraic square name ('e4') to 0-63 index.
 * a1=0, h1=7, a8=56, h8=63  — matches python-chess numbering.
 */
export function squareIndex(sq: string): number {
  return (parseInt(sq[1]) - 1) * 8 + (sq.charCodeAt(0) - 97);
}
